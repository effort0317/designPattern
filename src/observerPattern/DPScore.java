package observerPattern;

public class DPScore {
	private String name; // 디자인 패턴 점수 이름
	private int score;  // 디자인 패턴 점수
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}

}
